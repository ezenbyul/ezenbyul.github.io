package ex2;

public class Int1 {
	public static void main(String[] args) {
		int v1 = 10;
		int v2 = 012;
		int v3 = 0xA;
		int v4 = 0b1010;
		short v5 = 32000;
		
		System.out.println("Int1");
		
		System.out.println("v1="+v1);
		System.out.println("8진수 12의 v2="+v2);
		System.out.println("16진수 0xA의 v3="+v3);
		System.out.println("2진수 1010의 v4="+v4);
	}
}
